#include <iostream>
#include <string>

#include <gtest/gtest.h>

////////////////////////////////////////////////////////////////////////////////

TEST(GTestAssertStr, AssertEQ)
{
    ASSERT_EQ(std::string("1"), std::string("1"));
}

////////////////////////////////////////////////////////////////////////////////

TEST(GTestAssertStr, AssertNE)
{
    ASSERT_NE(std::string("1"), std::string("2"));
}

////////////////////////////////////////////////////////////////////////////////

TEST(GTestAssertStr, AssertStrEQ)
{
    ASSERT_STREQ("a", "a");
    //ASSERT_STREQ("A", "a"); // Fails
}

////////////////////////////////////////////////////////////////////////////////

TEST(GTestAssertStr, AssertStrNE)
{
    ASSERT_STRNE("a", "b");
}

////////////////////////////////////////////////////////////////////////////////

TEST(GTestAssertStr, AssertStrCaseEQ)
{
    ASSERT_STRCASEEQ("A", "a");
}

////////////////////////////////////////////////////////////////////////////////

TEST(GTestAssertStr, AssertStrCaseNE)
{
    ASSERT_STRCASENE("A", "b");
}
