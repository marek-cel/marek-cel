#!/usr/bin/env python3

################################################################################

import numpy as np

################################################################################

arr1 = np.array([10, 11, 12, 13, 14, 15])
arr2 = np.array([20, 21, 22, 23, 24, 25])

print(arr1)
print(arr2)

################################################################################

arr3 = np.add(arr1, arr2)

print()
print(arr3)

################################################################################

arr4 = np.subtract(arr1, arr2)

print()
print(arr4)

################################################################################

arr5 = np.multiply(arr1, arr2)

print()
print(arr5)

################################################################################

arr6 = np.divide(arr1, arr2)

print()
print(arr6)

################################################################################

arr6 = np.power(arr1, arr2)

print()
print(arr6)

