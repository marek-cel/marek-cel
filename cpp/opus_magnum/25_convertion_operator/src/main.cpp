class Complex
{
public:

    Complex( double re, double im )
    {
        _re = re;
        _im = im;
    }

    operator double() const
    {
        return _re;
    }

    double _re { 0.0 };
    double _im { 0.0 };
};

////////////////////////////////////////////////////////////////////////////////

class A
{
public:

    explicit operator double() const
    {
        return 0.0;
    }
};

////////////////////////////////////////////////////////////////////////////////

void fun( double x )
{
    cout << __func__ << " " << x << endl;
}

