#include <iostream>

////////////////////////////////////////////////////////////////////////////////

using namespace std;

////////////////////////////////////////////////////////////////////////////////

enum EnumA
{
    EnumA_0 = 0,
    EnumA_1,
    EnumA_2
};

////////////////////////////////////////////////////////////////////////////////

enum class EnumB : int
{
    EnumB_0 = 0,
    EnumB_1,
    EnumB_2
};

////////////////////////////////////////////////////////////////////////////////

int main()
{
    EnumA a = EnumA_1;
    EnumB b = EnumB::EnumB_2;

    cout << a << endl;
    cout << (int)b << endl;

    return 0;
}
