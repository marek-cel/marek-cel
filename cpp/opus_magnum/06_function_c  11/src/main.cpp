#include <iostream>

////////////////////////////////////////////////////////////////////////////////

using namespace std;

////////////////////////////////////////////////////////////////////////////////

int add( int a, int b )
{
    return a + b;
}

auto multiply( int a, int b ) -> int
{
    return a * b;
}

////////////////////////////////////////////////////////////////////////////////

void fun( double a )
{
    cout << "fun( double a )" << endl;
}

////////////////////////////////////////////////////////////////////////////////

void fun( ... )
{
    cout << "fun( ... )" << endl;
}

////////////////////////////////////////////////////////////////////////////////

class A
{
public:

    static auto add( int a, int b ) -> int;
};

auto A::add( int a, int b ) -> int
{
    return a + b;
}

////////////////////////////////////////////////////////////////////////////////

int main()
{
    auto a = add( 1, 2 );
    auto b = multiply( 1, 2 );

    auto c = A::add( 2, 3 );

    cout << "a= " << a << endl;
    cout << "b= " << b << endl;
    cout << "c= " << c << endl;

    fun( 3.14 );
    fun( 3.14, 7 );
    fun();

    return 0;
}
