#include <iostream>

////////////////////////////////////////////////////////////////////////////////

using namespace std;

////////////////////////////////////////////////////////////////////////////////

class A
{
public:
    int _x;
};

////////////////////////////////////////////////////////////////////////////////

class B
{
public:
    B() { cout << "B::B()" << endl; }
    B( const B& ) { cout << "B::B(const B&)" << endl; }
    int _x;
};

////////////////////////////////////////////////////////////////////////////////

B fun()
{
    B b;
    b._x = 0;
    return b;
}

////////////////////////////////////////////////////////////////////////////////

int main()
{
    cout << "Hello World!" << endl;

    /////////////////////////////////

    A a1;
    a1._x = 1;

    A a2( a1 );

    cout << a1._x << " " << a2._x << endl;

    /////////////////////////////////

    B b;
    b = fun();

    cout << b._x << endl;

    /////////////////////////////////

    return 0;
}
