#include <iostream>

////////////////////////////////////////////////////////////////////////////////

using namespace std;

////////////////////////////////////////////////////////////////////////////////

class A
{
public:
    A( int x ) { _x = x; }
    A( const A &a ) { _x = a._x; }
    int getX() { return _x; }
private:
    int _x {};
};

////////////////////////////////////////////////////////////////////////////////

class B
{
public:
    explicit B( int x ) { _x = x; }
    B( const B &b ) { _x = b._x; }
    int getX() { return _x; }
private:
    int _x;
};

////////////////////////////////////////////////////////////////////////////////

class CA
{
public:
    CA( A a ) : _a( a ) {}
    int getX() { return _a.getX(); }
private:
    A _a;
};

////////////////////////////////////////////////////////////////////////////////

class CB
{
public:
    CB( B b ) : _b( b ) {}
    int getX() { return _b.getX(); }
private:
    B _b;
};

////////////////////////////////////////////////////////////////////////////////

int main()
{
    cout << "Hello World!" << endl;

    CA ca( 1 );
    //CB cb( 2 ); // error!

    cout << ca.getX() << endl;
    //cout << cb.getX() << endl;

    return 0;
}
