#include <AbstractClass.h>

////////////////////////////////////////////////////////////////////////////////

AbstractClass::AbstractClass()
{
    constexpr int size = 10;

    _foo = new int[ size ];

    for ( int i = 0; i < size; ++i )
    {
        _foo[i] = i+1;
    }
}

////////////////////////////////////////////////////////////////////////////////

AbstractClass::~AbstractClass()
{
    if ( _foo ) delete [] _foo;
}

////////////////////////////////////////////////////////////////////////////////

int AbstractClass::getOne()
{
    return 1;
}

////////////////////////////////////////////////////////////////////////////////

int AbstractClass::getTwo()
{
    return 2;
}
