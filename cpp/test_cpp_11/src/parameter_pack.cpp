#include <iostream>
#include <memory>

////////////////////////////////////////////////////////////////////////////////

using namespace std;

////////////////////////////////////////////////////////////////////////////////

template < class... T >
auto add( T... args )
{
    return ( ... + args );
}

////////////////////////////////////////////////////////////////////////////////

template < class T, class... Args >
auto make_unique( Args... args )
{
    return std::unique_ptr<T>( new T(std::forward<Args>(args)... ) );
}

////////////////////////////////////////////////////////////////////////////////

int main()
{
    cout << __cplusplus << endl;

    cout << add( 1.1, 2.2, 3.14 ) << endl;

    return 0;
}
